angular.module("aTwitter",[
	"pages",
	"components",
	"services",
	"ui.router",
	"ngResource",
	"ui.bootstrap",
	])
 .config(function($stateProvider,$urlRouterProvider){
 	$stateProvider
 		.state("signIn",{
 			url:"/signIn",
 			templateUrl:"./js/pages/signIn/index.html",
 			controller:"SignInController",
 			resolve:{
 				checkLogin:function($location){
 					if(localStorage.Identifier)
 						$location.path("/")
 				}
 			}
 		})
 		.state("signUp",{
 			url:"/signUp",
 			templateUrl:"./js/pages/signUp/index.html",
 			controller:"SignUpController",
 			resolve:{
 				checkLogin:function($location){
 					if(localStorage.Identifier)
 						$location.path("/")
 				}
 			}
 		})
 		.state("index",{
 			url:"",
 			templateUrl:"./js/pages/index/index.html",
 			controller:"IndexController",
 			resolve:{
 				checkLogin:function($location){
 					if(!localStorage.Identifier)
 						$location.path("/signIn")
 					else{
 						$location.path("/home")
 					}
 				}
 			}
 		})
 		.state("index.home",{
 			url:"/home",
 			templateUrl:"./js/pages/home/index.html",
 			controller:"HomeController"
 		})
 		.state("index.notifications",{
 			url:"/notifications",
 			templateUrl:"./js/pages/notifications/index.html",
 			controller:"NotificationsController",
 		})
 		.state("index.profile",{
 			url:"/:userHandle",
 			templateUrl:"./js/pages/profile/index.html",
 			controller:"ProfileController"
 		})
 })